//Operadores de Atribuição
public class Operador {
	public static void main(String[] args){
		//Ex
		int x = 10;
		//Queremos atribuir + 3 no valor da variavel
		x = x + 3;

		//Podemos diminuir o codigo, utilizando os operadores de atribuição
		int y = 10;
		//Adcionando mais 10
		y+=10;
		//multiplicando mais 10
		y*=10;
		//dividindo por 1o
		y/=10;

	}

}