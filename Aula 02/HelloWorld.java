public class HelloWorld {

	//Java começa a execução com o metodo Main
	public static void main(String[] args) {
		
		//Sequencias de Escape: tab(\t), quebraDeLinha(\n)
		//adicionar barra(\) para adicionar aspas ou barras
		System.out.println("\tHelloWorld");
		System.out.println("Hello\nWorld");
		System.out.println("\"Hello\"World");

		System.out.println("HelloWorld"); // imprime em uma linha
		System.out.print("Hello World");
		//Na saida do sistema operacional imprima "Hello World"
	}
}