/*
Constantes
final = Indica uma variavel contante(que nunca muda)
*/
public class Variavel {

	public static void main(String[] args) {

		int populaçãoBrasileira = 100; //Tem que ser uma variavel pois  muda
		final double PI = 3.14; // É uma variavel constante, pois nunca se altera
		final char SEXO_MASCULINO = 'M'; //Em constantes, variaveis são escritas com LETRA MAIUSCULA
	}
}